  id int(11) NOT NULL AUTO_INCREMENT,
  name varchar(255) DEFAULT NULL,
  sport_id int(11) NOT NULL,
  PRIMARY KEY (id),
  KEY sport_id (sport_id),
  CONSTRAINT team_ibfk_1 FOREIGN KEY (sport_id) REFERENCES sport (id)
);

